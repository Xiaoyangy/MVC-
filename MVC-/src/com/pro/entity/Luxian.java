package com.pro.entity;

public class Luxian {
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getQishi() {
		return qishi;
	}
	public void setQishi(String qishi) {
		this.qishi = qishi;
	}
	public String getZhongzhi() {
		return zhongzhi;
	}
	public void setZhongzhi(String zhongzhi) {
		this.zhongzhi = zhongzhi;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	private int id;
	private String qishi;
	private String zhongzhi;
	private int status;
}
